    <h2>Introduction</h2>
    <p>Welcome to Solar System Ephemeris ("we," "our," or "us"). We are committed to protecting your privacy and ensuring transparency
       about how we handle your personal information. This Privacy Policy explains how we collect, use, and store your information
       when you use our tool available at <a href="https://crteknologies.fr/tools/ephemeris">https://crteknologies.fr/tools/ephemeris</a>
       (the "Service").</p>
    <p>Our general approach is to collect only the information necessary to provide our Service. We adhere to a privacy-by-design
       philosophy, ensuring that, wherever possible, information is kept on your device and not shared with our servers.</p>

    <h2>Information We Collect and How We Use It</h2>
    <h3>Preferences</h3>
    <p>Our Service allows you to customize certain settings, such as language preferences and theme choices. These settings are
       stored locally in your browser's local storage. This means that:</p>
    <ul>
        <li>The data is stored only on your device.</li>
        <li>We do not have access to or control over this data.</li>
        <li>The data has no impact on the requests to the servers, thus we cannot even infer them indirectly.</li>
    </ul>

    <h3>System settings</h3>
    <p>Our Service may access your system settings (system language and timezone settings) through JavaScript to provide a more
       tailored experience.
       The information is used solely within the current session of your browser and is not stored or accessible or inferable by
       our servers.</p>

    <h3>Positioning Service</h3>
    <p>Our Service may use your device's positioning service to provide location-based functionality. This is done only when you
       click on the dedicated button to request your location.
       The information is used solely within the current session of your browser and is not stored or accessible or inferable by
       our servers.</p>

    <h3>Server Access Logs</h3>
    <p>When you access our Service, our servers automatically collects standard access logs. These logs may include:</p>
    <ul>
        <li>IP address</li>
        <li>Browser type</li>
        <li>Operating system</li>
        <li>Date and time of access</li>
        <li>The pages you visit on our Service</li>
    </ul>
    <p>The information collected in server access logs is used to enhance security by identifying and preventing unauthorized
       access, and to compile anonymous usage statistics to understand how our Service is used, such as the number of requests
       and the most accessed pages.</p>

    <h3>Use of jsDelivr CDN</h3>
    <p>For efficiency and performance reasons, we load some resources, such as JavaScript libraries, from the CDN jsDelivr. jsDelivr
       is cautious with privacy concerns and does not store any user data or track users. You can view their Data Processing
       Agreement (DPA) <a href="https://www.jsdelivr.com/documents/data-processing-agreement.pdf" target="_blank">here</a>.
       jsDelivr processes IP addresses to deliver content efficiently, but this data is aggregated and deleted quickly. They do not
       use cookies or similar tracking technologies.</p>

    <h2>Data Sharing and Disclosure</h2>
    <p>We do not share, sell, or rent your personal information to third parties. Access logs are used internally and are not shared
       with external parties, except as required by law or to protect our rights.</p>

